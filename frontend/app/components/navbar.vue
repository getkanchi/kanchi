<template>
  <div class="sticky top-0 z-50 bg-background-surface border-b border-border backdrop-blur-sm bg-opacity-95">
    <div class="px-6">
      <div class="flex h-14 items-center">
        <div class="flex items-center gap-6">
          <!-- Logo -->
